/**
 * Seed all establishments into the database
 * @param {Function} dbRunFn - promisified db.run(sql, params)
 * @param {Function} dbGetFn - promisified db.get(sql, params)
 */
export async function seedEtablissementsAgrees(dbRunFn, dbGetFn) {
  // Check if already seeded
  const count = await dbGetFn("SELECT COUNT(*) as cnt FROM etablissements_agrees");
  if (count && count.cnt > 0) {
    logger.info(`📦 Etablissements agrees already seeded (${count.cnt} records)`);
    return;
  }

  logger.info(`🌱 Seeding ${rawData.length} etablissements agrees...`);

  let inserted = 0;
  for (let i = 0; i < rawData.length; i++) {
    const [sigle, denomination, statut, territoire, province, etat] = rawData[i];
    const type = getType(denomination);
    const id = uuidv4();

    try {
      await dbRunFn(
        `INSERT INTO etablissements_agrees (id, sigle, denomination, statut, territoire, province, etat, type, ordre)
         VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?)`,
        [id, sigle, denomination, statut, territoire, province, etat, type, i + 1]
      );
      inserted++;
    } catch (err) {
      logger.warn(`  ⚠️ Failed to insert ${sigle}: ${err.message}`);
    }
  }

  logger.info(`✅ Seeded ${inserted}/${rawData.length} etablissements agrees`);
}
